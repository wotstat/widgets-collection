<template>
  <FallbackImg :src="targetUrl" :fallback="fallbackUrl" :loading />
</template>


<script setup lang="ts">
import FallbackImg from '@/components/shared/FallbackImg.vue'
import { GameVendor, RankImageDefinition, rankImageUrl } from '../utils'
import { computed } from 'vue'


const props = defineProps<{
  rank: RankImageDefinition
  game?: GameVendor
  season?: 'latest' | (string & {})
  size?: 'small' | 'medium' | 'large'
  loading?: 'lazy' | 'eager'
}>()

const targetUrl = computed(() => rankImageUrl(props.rank, props.size, props.game, props.season))
const fallbackUrl = computed(() => rankImageUrl(props.rank, props.size, props.game, 'latest'))
</script>


<style lang="scss" scoped></style>